// models/Review.js

const mongoose = require("mongoose");

const reviewSchema = new mongoose.Schema({
  userEmail: {
    type: String,
    required: true,
  },
  productTitle: {
    type: String,
    required: true,
  },
  rating: {
    type: Number,
    required: true,
    min: 1,
    max: 5,
  },
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

const Review = mongoose.model("Review", reviewSchema);

module.exports = Review;
